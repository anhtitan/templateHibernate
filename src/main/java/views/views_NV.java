    public void LoadData() {
        tblModel = (DefaultTableModel) tbNhanVien.getModel();
        tblModel.setColumnIdentifiers(new Object[]{"Mã", "Họ Tên", "Địa Chỉ", "Giới Tính", "Chức Vụ", "Cửa Hàng"});
        tblModel.setRowCount(0);

        for (NhanVien x : nhanVienService.getAll()) {
            tblModel.addRow(new Object[]{
                x.getMa(),
                x.getHo() + " " + x.getTenDem() + " " + x.getTen(),
                x.getDiaChi(),
                x.getGioiTinh(),
                x.getChucVu(),
                x.getCuaHang()
            });
        }
    }
