    private void loadComboCH() {
        comboCH = (DefaultComboBoxModel) cbCHang.getModel();
        comboCH.removeAllElements();
        for (CuaHang x : cuaHangService.getAll()) {
            comboCH.addElement(x);
        }
    }

    private void loadComboNV() {
        comboCV = (DefaultComboBoxModel) cbChucVu.getModel();
        comboCV.removeAllElements();
        for (ChucVu x : chucVuService.getAll()) {
            comboCV.addElement(x);
        }
    }

