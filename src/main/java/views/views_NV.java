    private void tbNhanVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbNhanVienMouseClicked
        int index = tbNhanVien.getSelectedRow();
        NhanVien nv = nhanVienService.getAll().get(index);
        txtMa.setText(nv.getMa());
        txtHo.setText(nv.getHo());
        txtTenDem.setText(nv.getTenDem());
        txtTen.setText(nv.getTen());
        txtDiaChi.setText(nv.getDiaChi());
        if (nv.getGioiTinh().equalsIgnoreCase("Nam")) {
            rdNam.setSelected(true);
        } else {
            rdNu.setSelected(true);
        }
        comboCH.setSelectedItem(nv.getCuaHang());
        comboCV.setSelectedItem(nv.getChucVu());
    }//GEN-LAST:event_tbNhanVienMouseClicked

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        NhanVien nv = new NhanVien();
        if (nhanVienService.getObjbyMa(txtMa.getText()) != null) {
            JOptionPane.showMessageDialog(this, "trung ma");
        } else {
            CuaHang ch = (CuaHang) comboCH.getSelectedItem();
            ChucVu cv = (ChucVu) comboCV.getSelectedItem();
            nv.setChucVu(cv);
            nv.setCuaHang(ch);
            nv.setDiaChi(txtDiaChi.getText());
            nv.setHo(txtHo.getText());
            nv.setTen(txtTen.getText());
            nv.setTenDem(txtTenDem.getText());
            if (rdNam.isSelected()) {
                nv.setGioiTinh("Nam");
            } else {
                nv.setGioiTinh("Nữ");
            }
            nv.setMa(txtMa.getText());
            if (nhanVienService.save(nv)) {
                JOptionPane.showMessageDialog(this, "oke");
            } else {
                JOptionPane.showMessageDialog(this, "error");
            }
            LoadData();
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        int index = tbNhanVien.getSelectedRow();
        NhanVien nv = nhanVienService.getObjbyMa((String) tbNhanVien.getValueAt(index, 0));
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "vui lòng chọn dòng cần cập nhật");
        } else {
            CuaHang ch = (CuaHang) comboCH.getSelectedItem();
            ChucVu cv = (ChucVu) comboCV.getSelectedItem();
            nv.setChucVu(cv);
            nv.setCuaHang(ch);
            nv.setDiaChi(txtDiaChi.getText());
            nv.setHo(txtHo.getText());
            nv.setTen(txtTen.getText());
            nv.setTenDem(txtTenDem.getText());
            if (rdNam.isSelected()) {
                nv.setGioiTinh("Nam");
            } else {
                nv.setGioiTinh("Nữ");
            }
            nv.setMa(txtMa.getText());
            if (nhanVienService.save(nv)) {
                JOptionPane.showMessageDialog(this, "oke");
            } else {
                JOptionPane.showMessageDialog(this, "error");
            }
            LoadData();
        }
    }//GEN-LAST:event_btnSuaActionPerformed

